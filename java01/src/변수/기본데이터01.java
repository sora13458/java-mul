package 변수;

public class 기본데이터01 {

	public static void main(String[] args) {
		
//		정수,실수,단일문자, 논리
//		ram에 넣어보자
		
		int age = 100;
		double hight = 162.5;
		char gender = '남'; //문자 한개는 'x'
		boolean food = true; //논리데이터
		
		String name = "홍길동";
		
		System.out.println("나의 나이는 :" + age);
		System.out.println("나의 키는 :" + hight);
		System.out.println("나의 성별은 : " + gender);
		System.out.println("오늘의 식사여부 : " + food);
		System.out.println("나의 이름은 : " + name);
	}	

}
